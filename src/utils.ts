//checks the validity of the entered ISIN
export const checkISINValidity = (isin: string) => {
  //no spaces
  isin.replace(/\s/g, "");
  //12 characters
  if (isin.length !== 12) return false;
  //first two letters must be a country code
  if (countryCodes.indexOf(isin.slice(0, 2).toUpperCase()) === -1) return false;
  //only alphanumerics in 9 middle characters
  if (!/^[a-zA-Z0-9]+$/.test(isin.slice(2, 11))) return false;
  //only digit in the last character
  if (!/^\d+$/.test(isin.slice(11))) return false;
  return true;
};

//checks if the user has already subscribed to the entered ISIN
export const checkISINUniqueness = (isin: string) => {
  const subscribedISINs: string[] = JSON.parse(localStorage.getItem("ISINs"));
  if (subscribedISINs.length !== 0)
    for (let i = 0; i < subscribedISINs.length; i++) {
      if (
        subscribedISINs[i].toLowerCase().normalize() ===
        isin.toLowerCase().normalize()
      )
        return false;
    }
  return true;
};

//stores the new ISIN in localStorage
export const addISINToList = (isin: string) => {
  let ISINs: string[] = JSON.parse(localStorage.getItem("ISINs"));
  ISINs = [...ISINs, isin];
  localStorage.setItem("ISINs", JSON.stringify(ISINs));
};

//removes the ISIN from localstorage
export const removeISINFromList = (isin: string) => {
  let ISINs: string[] = JSON.parse(localStorage.getItem("ISINs"));
  ISINs = ISINs.filter((singleISIN) => {
    return singleISIN !== isin;
  });
  localStorage.setItem("ISINs", JSON.stringify(ISINs));
};

